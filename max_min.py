# max_min_sort.py
# Demonstrates the max(), min() and sorted() functions

listOfNumbers = [43,1,22,0,100,4,19,93,29,44,55,65,75,15,95]

# max
print("Max: ",max(listOfNumbers))

# min
print("Min: ",min(listOfNumbers))

# sum
print("Min: ",sum(listOfNumbers))

# sorted
print("Sorted: ",sorted(listOfNumbers))
# or
listOfNumbers.sort()
print("Sorted: ",listOfNumbers)

# sorted in reverse
print("Sorted in Reverse: ", sorted(listOfNumbers,reverse=True))
